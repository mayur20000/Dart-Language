/*
Explicit Type Declaration:
You can explicitly state the type of the variable.
 This improves readability and helps prevent type-related errors.
It's often preferred for clarity.
 */

void main(){
  String studentName = "Bob";
  int studentage = 22;
  double studentGPA = 8.6;
  bool isEnrolled = true;

  print('studentName: $studentName');
  print('studentAge: $studentage');
  print('studentGPA: $studentGPA');
  print('isEnrolled: $isEnrolled');
}